let a = 10;
let b = 3;
let name = "Alice";
// Arithmetic operations
let sum = a + b;
let diff = a - b;
let mul = a * b;
let div = a / b;
let mod = a % b;

// String operation
let greeting = "Hello, " + name + "!";

// Comparisons
let isGreater = a > b;     
let isEqual = a === b;

// Logical operations
let condition1 = (a > 5);
let condition2 = (b > 5);

let andResult = condition1 && condition2;
let orResult  = condition1 || condition2;
let notResult = !condition1;

console.log("sum =", sum);
console.log("diff =", diff);
console.log("mul =", mul);
console.log("div =", div);
console.log("mod =", mod);
console.log("name =", name);
console.log("greeting =", greeting);
console.log("isGreater =", isGreater);
console.log("isEqual =", isEqual);
console.log("AND result =", andResult);
console.log("OR result =", orResult);
console.log("NOT result =", notResult);